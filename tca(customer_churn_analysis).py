df.columns.values

columns = [
    'PhoneService', 'MultipleLines', 'InternetService',
    'OnlineSecurity', 'OnlineBackup', 'DeviceProtection',
    'TechSupport', 'StreamingTV', 'StreamingMovies'
]

# create subplot grid
fig, axes = plt.subplots(3, 3, figsize=(18, 12))

# flatten axes for easier looping
axes = axes.flatten()

for i, col in enumerate(columns):
    sns.countplot(x=col, data=df, hue="Churn", ax=axes[i])
    axes[i].set_title(f"Churn by {col}")
    axes[i].tick_params(axis='x', rotation=45)

plt.tight_layout()
plt.show()

"""**The majority of customerss who do not churn tend to have servies like PhoneServices,InternetService(particularly DSL) and OnlineSecurity enabled.For servies like OnlineBackup, TechSupport, and StreamingTV, churn rates are noticeably higher when these services are not used or are unavailable.**"""

