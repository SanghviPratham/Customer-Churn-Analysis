import pandas as pd
import matplotlib.pyplot as plt
import numpy as np
import seaborn as sns

df = pd.read_csv("/content/Customer Churn.csv")

df.head()

df.tail()

df.info()

df.isnull().sum()

"""**Replacing blank with 0 as tenure is 0 and no total charges are recorded**"""

df["TotalCharges"] = df["TotalCharges"].replace(" ","0")
df["TotalCharges"] = df["TotalCharges"].astype("float")

df.info()

df.shape

df.describe()

df.duplicated().sum()

df["customerID"].duplicated().sum()

"""Converted 0 and 1 value of senior citizen to yes/no to make it easier to understand"""

def conv(value):
  if value == 1:
    return "yes"
  else:
    return "no"

df["SeniorCitizen"] = df["SeniorCitizen"].apply(conv)

df.head(10)

ax  = sns.countplot(x = df["Churn"] ,data = df)

ax.bar_label(ax.containers[0])
plt.title("Count of Customers by Churn")
plt.show()

gb = df.groupby("Churn").agg({"Churn":"count"})
gb
plt.pie(gb["Churn"],labels = gb.index, autopct = "%1.2f%%" )
plt.title("Percentage Of Churned Customers")
plt.show()

"""From the given pie chart we can conclude that 26.54% of our customers have churned out. lets's explore the reason behind it"""

#second option to do above one
