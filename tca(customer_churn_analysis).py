plt.show()

# Create grouped data in percentage
gb = df.groupby(["SeniorCitizen","Churn"]).size().unstack()
gb_percent = gb.div(gb.sum(axis=1), axis=0) * 100

# Plot stacked bar chart
ax = gb_percent.plot(kind="bar", stacked=True)

# Add percentage labels
for container in ax.containers:
    labels = [f"{v:.1f}%" if v > 0 else "" for v in container.datavalues]
    ax.bar_label(container, labels=labels, label_type="center")

plt.title("Churn Percentage by Senior Citizen")
plt.xlabel("Senior Citizen")
plt.ylabel("Percentage")

plt.show()

"""**Comparative a greated percentage of people in senior citizen category have churned**"""

sns.histplot(x = "tenure",data = df, bins = 72,hue="Churn")
